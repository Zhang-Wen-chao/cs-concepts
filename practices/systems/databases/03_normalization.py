"""
范式分解：1NF → 2NF → 3NF → BCNF

例子：选课系统
学号 → 姓名、院系
院系 → 院长
(学号, 课程) → 成绩
"""


def print_schema(name, relations):
    print(f"\n{'=' * 40}")
    print(f"{name}")
    print(f"{'=' * 40}")
    for rel_name, attrs, fds in relations:
        print(f"  R({', '.join(attrs)})")
        print(f"    FD: {', '.join(f'{{k}}→{{v}}' for k, v in fds)}")


if __name__ == "__main__":
    # 初始：所有属性在一个表
    print("范式分解演示：选课系统\n")

    unnormalized = [
        ("选课表", ["学号", "姓名", "院系", "院长", "课程", "成绩"],
         [("{学号}", "{姓名, 院系}"),
          ("{院系}", "{院长}"),
          ("{学号, 课程}", "{成绩}")])
    ]
    print_schema("1. 未范式化（1NF 前）", unnormalized)
    print("  问题：数据冗余（每个学生每门课都重复姓名、院系、院长）")

    # 已经是 1NF（原子列）
    nf1 = [
        ("R1", ["学号", "姓名", "院系", "院长", "课程", "成绩"],
         [("{学号}", "{姓名, 院系}"),
          ("{院系}", "{院长}"),
          ("{学号, 课程}", "{成绩}")])
    ]
    print_schema("2. 1NF（属性原子化，但未处理部分依赖）", nf1)
    print("  问题：学号→姓名 是部分依赖（依赖主键的一部分）")

    # 2NF：去掉部分依赖
    nf2 = [
        ("学生", ["学号", "姓名", "院系", "院长"],
         [("{学号}", "{姓名, 院系}"),
          ("{院系}", "{院长}")]),
        ("成绩", ["学号", "课程", "成绩"],
         [("{学号, 课程}", "{成绩}")])
    ]
    print_schema("3. 2NF（消除部分依赖）", nf2)
    print("  问题：院系→院长 是传递依赖（学号→院系→院长）")

    # 3NF：去掉传递依赖
    nf3 = [
        ("学生", ["学号", "姓名", "院系"],
         [("{学号}", "{姓名, 院系}")]),
        ("院系", ["院系", "院长"],
         [("{院系}", "{院长}")]),
        ("成绩", ["学号", "课程", "成绩"],
         [("{学号, 课程}", "{成绩}")])
    ]
    print_schema("4. 3NF（消除传递依赖）", nf3)
    print("  3NF 已足够大多数应用场景，BCNF 更进一步处理所有非平凡 FD")
